from ansible.module_utils.basic import AnsibleModule


def login(data, fos):
    host = data['host']
    username = data['username']
    password = data['password']

    fos.debug('on')
    if 'https' in data and not data['https']:
        fos.https('off')
    else:
        fos.https('on')

    fos.login(host, username, password)


def filter_switch_controller_custom_command_data(json):
    option_list = ['command', 'command-name', 'description']
    dictionary = {}

    for attribute in option_list:
        if attribute in json and json[attribute] is not None:
            dictionary[attribute] = json[attribute]

    return dictionary


def flatten_multilists_attributes(data):
    multilist_attrs = []

    for attr in multilist_attrs:
        try:
            path = "data['" + "']['".join(elem for elem in attr) + "']"
            current_val = eval(path)
            flattened_val = ' '.join(elem for elem in current_val)
            exec(path + '= flattened_val')
        except BaseException:
            pass

    return data


def switch_controller_custom_command(data, fos):
    vdom = data['vdom']
    switch_controller_custom_command_data = data['switch_controller_custom_command']
    flattened_data = flatten_multilists_attributes(switch_controller_custom_command_data)
    filtered_data = filter_switch_controller_custom_command_data(flattened_data)
    if switch_controller_custom_command_data['state'] == "present":
        return fos.set('switch-controller',
                       'custom-command',
                       data=filtered_data,
                       vdom=vdom)

    elif switch_controller_custom_command_data['state'] == "absent":
        return fos.delete('switch-controller',
                          'custom-command',
                          mkey=filtered_data['command-name'],
                          vdom=vdom)


def fortios_switch_controller(data, fos):
    login(data, fos)

    if data['switch_controller_custom_command']:
        resp = switch_controller_custom_command(data, fos)

    fos.logout()
    return not resp['status'] == "success", resp['status'] == "success", resp


def main():
    fields = {
        "host": {"required": True, "type": "str"},
        "username": {"required": True, "type": "str"},
        "password": {"required": False, "type": "str", "no_log": True},
        "vdom": {"required": False, "type": "str", "default": "root"},
        "https": {"required": False, "type": "bool", "default": True},
        "switch_controller_custom_command": {
            "required": False, "type": "dict",
            "options": {
                "state": {"required": True, "type": "str",
                          "choices": ["present", "absent"]},
                "command": {"required": False, "type": "str"},
                "command-name": {"required": True, "type": "str"},
                "description": {"required": False, "type": "str"}

            }
        }
    }

    module = AnsibleModule(argument_spec=fields,
                           supports_check_mode=False)
    try:
        from fortiosapi import FortiOSAPI
    except ImportError:
        module.fail_json(msg="fortiosapi module is required")

    fos = FortiOSAPI()

    is_error, has_changed, result = fortios_switch_controller(module.params, fos)

    if not is_error:
        module.exit_json(changed=has_changed, meta=result)
    else:
        module.fail_json(msg="Error in repo", meta=result)


if __name__ == '__main__':
    main()
